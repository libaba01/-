public class Bean {
    private String key;
    private int count;
    private double p;
    public Bean(String key, int count) {
        this.key = key;
        this.count = count;
    }

    public int getCount() {
        return count;
    }

    public void setCount(int count) {
        this.count = count;
    }

    public double getP() {
        return p;
    }

    public void setP(double p) {
        this.p = p;
    }


}
